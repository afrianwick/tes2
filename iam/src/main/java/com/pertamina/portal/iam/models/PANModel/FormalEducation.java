package com.pertamina.portal.iam.models.PANModel;

public class FormalEducation {

    private String startDate, endDate, educationalEstablishment;
    private String institueLocation, branchOfStudy, action;

    public String getStartDate() {
        return startDate;
    }

    public void setStartDate(String startDate) {
        this.startDate = startDate;
    }

    public String getEndDate() {
        return endDate;
    }

    public void setEndDate(String endDate) {
        this.endDate = endDate;
    }

    public String getEducationalEstablishment() {
        return educationalEstablishment;
    }

    public void setEducationalEstablishment(String educationalEstablishment) {
        this.educationalEstablishment = educationalEstablishment;
    }

    public String getInstitueLocation() {
        return institueLocation;
    }

    public void setInstitueLocation(String institueLocation) {
        this.institueLocation = institueLocation;
    }

    public String getBranchOfStudy() {
        return branchOfStudy;
    }

    public void setBranchOfStudy(String branchOfStudy) {
        this.branchOfStudy = branchOfStudy;
    }

    public String getAction() {
        return action;
    }

    public void setAction(String action) {
        this.action = action;
    }
}
