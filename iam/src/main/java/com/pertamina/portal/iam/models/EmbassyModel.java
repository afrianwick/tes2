package com.pertamina.portal.iam.models;

public class EmbassyModel {

    private String embassyID, country, officialName, address, area, contact;

    public String getEmbassyID() {
        return embassyID;
    }

    public void setEmbassyID(String embassyID) {
        this.embassyID = embassyID;
    }

    public String getCountry() {
        return country;
    }

    public void setCountry(String country) {
        this.country = country;
    }

    public String getOfficialName() {
        return officialName;
    }

    public void setOfficialName(String officialName) {
        this.officialName = officialName;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getArea() {
        return area;
    }

    public void setArea(String area) {
        this.area = area;
    }

    public String getContact() {
        return contact;
    }

    public void setContact(String contact) {
        this.contact = contact;
    }
}
