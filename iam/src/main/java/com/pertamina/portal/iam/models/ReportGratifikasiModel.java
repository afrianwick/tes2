package com.pertamina.portal.iam.models;

public class ReportGratifikasiModel {

    private String id, date, value, nilaiGratifikasi, jumlah, jenis, peminta, alasan, tglPenisian, tglGratifikasi;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getValue() {
        return value;
    }

    public void setValue(String value) {
        this.value = value;
    }

    public String getNilaiGratifikasi() {
        return nilaiGratifikasi;
    }

    public void setNilaiGratifikasi(String nilaiGratifikasi) {
        this.nilaiGratifikasi = nilaiGratifikasi;
    }

    public String getJumlah() {
        return jumlah;
    }

    public void setJumlah(String jumlah) {
        this.jumlah = jumlah;
    }

    public String getJenis() {
        return jenis;
    }

    public void setJenis(String jenis) {
        this.jenis = jenis;
    }

    public String getPeminta() {
        return peminta;
    }

    public void setPeminta(String peminta) {
        this.peminta = peminta;
    }

    public String getAlasan() {
        return alasan;
    }

    public void setAlasan(String alasan) {
        this.alasan = alasan;
    }

    public String getTglPenisian() {
        return tglPenisian;
    }

    public void setTglPenisian(String tglPenisian) {
        this.tglPenisian = tglPenisian;
    }

    public String getTglGratifikasi() {
        return tglGratifikasi;
    }

    public void setTglGratifikasi(String tglGratifikasi) {
        this.tglGratifikasi = tglGratifikasi;
    }
}
