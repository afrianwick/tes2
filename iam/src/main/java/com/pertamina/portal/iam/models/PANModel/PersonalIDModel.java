package com.pertamina.portal.iam.models.PANModel;

public class PersonalIDModel {

    private String startDate, endDate, idType, idNumber, action;
    private String ukuranBaju, ukuranCelana, ukuranSepatu;

    public String getStartDate() {
        return startDate;
    }

    public void setStartDate(String startDate) {
        this.startDate = startDate;
    }

    public String getEndDate() {
        return endDate;
    }

    public void setEndDate(String endDate) {
        this.endDate = endDate;
    }

    public String getIdType() {
        return idType;
    }

    public void setIdType(String idType) {
        this.idType = idType;
    }

    public String getIdNumber() {
        return idNumber;
    }

    public void setIdNumber(String idNumber) {
        this.idNumber = idNumber;
    }

    public String getAction() {
        return action;
    }

    public void setAction(String action) {
        this.action = action;
    }

    public String getUkuranBaju() {
        return ukuranBaju;
    }

    public void setUkuranBaju(String ukuranBaju) {
        this.ukuranBaju = ukuranBaju;
    }

    public String getUkuranCelana() {
        return ukuranCelana;
    }

    public void setUkuranCelana(String ukuranCelana) {
        this.ukuranCelana = ukuranCelana;
    }

    public String getUkuranSepatu() {
        return ukuranSepatu;
    }

    public void setUkuranSepatu(String ukuranSepatu) {
        this.ukuranSepatu = ukuranSepatu;
    }
}
